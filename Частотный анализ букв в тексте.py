def count_letters(text):
    """
    Функция подсчитывает количество каждой буквы в переданном тексте.
    При подсчёте буквы верхнего и нижнего регистра считаются одинаковыми.
    Возвращает словарь с буквами в нижнем регистре и их количеством.
    """
    letter_counts = {}
    for char in text.lower():
        if char.isalpha():  # Проверяем, является ли символ буквой
            letter_counts[char] = letter_counts.get(char, 0) + 1
    return letter_counts

def calculate_frequency(letter_counts, total_letters):
    """
    Функция вычисляет частоту каждой буквы.
    Возвращает словарь с буквами в качестве ключей и их частотой (округлённой до 2 знаков после запятой) в качестве значений.
    """
    return {letter: round(count / total_letters, 2) for letter, count in letter_counts.items()}

# Текст для анализа
main_str = """
У лукоморья дуб зелёный;
Златая цепь на дубе том:
И днём и ночью кот учёный
Всё ходит по цепи кругом;
Идёт направо — песнь заводит,
Налево — сказку говорит.
Там чудеса: там леший бродит,
Русалка на ветвях сидит;
Там на неведомых дорожках
Следы невиданных зверей;
Избушка там на курьих ножках
Стоит без окон, без дверей;
Там лес и дол видений полны;
Там о заре прихлынут волны
На брег песчаный и пустой,
И тридцать витязей прекрасных
Чредой из вод выходят ясных,
И с ними дядька их морской;
Там королевич мимоходом
Пленяет грозного царя;
Там в облаках перед народом
Через леса, через моря
Колдун несёт богатыря;
В темнице там царевна тужит,
А бурый волк ей верно служит;
Там ступа с Бабою Ягой
Идёт, бредёт сама собой,
Там царь Кащей над златом чахнет;
Там русский дух… там Русью пахнет!
И там я был, и мёд я пил;
У моря видел дуб зелёный;
Под ним сидел, и кот учёный
Свои мне сказки говорил.
"""

# Подсчёт букв и их частот
letter_counts = count_letters(main_str)
total_letters = sum(letter_counts.values())
letter_frequencies = calculate_frequency(letter_counts, total_letters)

# Печать результатов в порядке их первого появления в тексте
seen_letters = set()
for char in main_str.lower():
    if char.isalpha() and char not in seen_letters:
        seen_letters.add(char)
        # Форматируем частоту с двумя знаками после запятой
        print(f"{char}: {letter_frequencies[char]:.2f}")
